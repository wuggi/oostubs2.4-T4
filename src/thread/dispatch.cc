Dispatcher::Dispatcher(){
    t_active = 0;
}

/**\~english \todo implement**/
void Dispatcher::go(Thread& first){

    if(!active())
    {
        t_active=first;
        go(first);
    }

}

/**\~english \todo implement**/
void Dispatcher::dispatch(Thread& next){

    Thread* running = (Thread*) t_active;
    t_active = next;
    running->resume(next);
}
